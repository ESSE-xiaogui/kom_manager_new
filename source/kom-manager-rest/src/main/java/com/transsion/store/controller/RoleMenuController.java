package com.transsion.store.controller;

import com.rest.service.controller.AbstractController;
import com.transsion.store.bo.RoleMenu;
import com.transsion.store.dto.RoleMenuDto;
import com.shangkang.core.dto.RequestModel;
import com.transsion.store.facade.RoleMenuFacade;
import com.shangkang.core.bo.Pagination;
import com.shangkang.core.exception.ServiceException;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;

import javax.ws.rs.*;
import javax.ws.rs.core.MediaType;
import java.util.List;

@Controller
@Path("roleMenu")
public class RoleMenuController extends AbstractController{

	@Autowired
	private RoleMenuFacade roleMenuFacade;
	
	/**
	 * 通过主键查询实体对象
	 * @param primaryKey
	 * @return
	 * @throws ServiceException
	 */
	@GET
	@Path("/getByPK")
	@Produces({MediaType.APPLICATION_JSON})
	public RoleMenu getByPK(@QueryParam("key") java.lang.Long primaryKey) throws ServiceException
	{
		return roleMenuFacade.getByPK(primaryKey);
	}

	/**
	* 分页查询记录
	* @return
	* @throws ServiceException
	*/
	@POST
	@Path("/listPg")
	@Consumes({MediaType.APPLICATION_JSON})
	@Produces({MediaType.APPLICATION_JSON})
	public Pagination<RoleMenu> listPgRoleMenu(RequestModel<RoleMenu> requestModel) throws ServiceException
	{
		Pagination<RoleMenu> pagination = new Pagination<RoleMenu>();

		pagination.setPaginationFlag(requestModel.isPaginationFlag());
		pagination.setPageNo(requestModel.getPageNo());
		pagination.setPageSize(requestModel.getPageSize());
		pagination.setParams(requestModel.getParams());
		pagination.setOrderBy(requestModel.getOrderBy());

		return roleMenuFacade.listPaginationByProperty(pagination, requestModel.getParams());
	}

	/**
	* 新增记录
	* @return
	* @throws ServiceException
	*/
	@POST
	@Path("/add")
	@Consumes({MediaType.APPLICATION_JSON})
	public void add(RoleMenu roleMenu) throws ServiceException
	{
		roleMenuFacade.save(roleMenu);
	}

	/**
	* 根据多条主键值删除记录
	* @return
	* @throws ServiceException
	*/
	@POST
	@Path("/delete")
	@Consumes({MediaType.APPLICATION_JSON})
	public void delete(List<java.lang.Long> primaryKeys) throws ServiceException
	{
		roleMenuFacade.deleteByPKeys(primaryKeys);
	}

	/**
	* 修改记录
	* @return
	* @throws ServiceException
	*/
	@PUT
	@Path("/update")
	@Consumes({MediaType.APPLICATION_JSON})
	public void update(RoleMenu roleMenu) throws ServiceException
	{
		roleMenuFacade.update(roleMenu);
	}
	
	/**
	* 新增菜单角色
	* @return
	* @throws ServiceException
	*/
	@POST
	@Path("/addMenuRole")
	@Consumes({MediaType.APPLICATION_JSON}) 
	public void saveMenuRole(RoleMenuDto roleMenuDto) throws ServiceException {
		 roleMenuFacade.saveMenuRole(this.getAuthorization(), roleMenuDto);
	}
}
